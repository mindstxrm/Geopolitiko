def _country_integration_scores(iso3: str, name: str, region: str, pop: int, area: int, density: int):
    """Generate deterministic per-country scores (unique per country, plausible ranges). Returns (trade_flow_pct_gdp, debt_distress_score, capital_flight_risk, reserve_months_imports, fx_vulnerability_score, energy_import_exposure_pct, geopolitical_fragility_score)."""
    h = hash((iso3, name, region)) & 0x7FFFFFFF
    # Use seed to get reproducible variation; scale by pop/area for slight realism
    small = (area < 50000 or pop < 1_500_000)
    large_economy = pop > 80_000_000
    trade = 28 + (h % 95)  # 28-122
    if small:
        trade = min(120, trade + 25)  # small countries often more trade-dependent
    debt = 30 + (h % 58)  # 30-87
    if large_economy:
        debt = min(55, debt - 10)
    cap_flight = 18 + ((h >> 7) % 58)  # 18-75
    reserve = round(2.0 + (h >> 14) % 160) / 10  # 2.0-17.0
    if large_economy:
        reserve = max(reserve, 8.0)
    fx = 15 + ((h >> 21) % 65)  # 15-79
    energy = 10 + ((h >> 26) % 88)  # 10-97
    if small:
        energy = min(95, energy + 25)
    geo = 30 + ((h >> 5) % 48)  # 30-77
    return (float(min(150, trade)), min(100, debt), min(100, cap_flight), round(reserve, 1), min(100, fx), float(min(100, energy)), min(100, geo))


def _build_integration_risk_full():
    """Build INTEGRATION_RISK_FULL: every country has its own 7-tuple (override or generated)."""
